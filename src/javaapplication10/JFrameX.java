    private void voiture() throws SQLException {
        
            
        String marq=marque.getText();
        String mod=modele.getText();
        String ref=reference.getText();
        String coule=couleur.getText();
        AdministationVoiture adm=new AdministationVoiture();
        boolean b=adm.AjouterVehicule(ref, marq, mod, coule);
        marque.setText("");
        modele.setText("");
        reference.setText("");
        couleur.setText("");
    }

    private void camionete() throws SQLException {
        String marq=marque.getText();
        String mod=modele.getText();
        String ref=reference.getText();
        String coule=couleur.getText();
        String charge=chargemax.getText();
        double ch=Double.parseDouble(charge);
        
        AdministrationCamionette adm=new AdministrationCamionette();
        boolean b=adm.AjouterVehicule(ref, marq, mod, coule,ch);
        marque.setText("");
        modele.setText("");
        reference.setText("");
        couleur.setText("");
        chargemax.setText("");
    }

    private void minibus() throws SQLException {
        String marq=marque.getText();
        String mod=modele.getText();
        String ref=reference.getText();
        String coule=couleur.getText();
        String nbrplaces=nbplaces.getText();
        int ch=Integer.parseInt(nbrplaces);
        
        AdministrationiMiniBus adm=new AdministrationiMiniBus();
        boolean b=adm.AjouterVehicule(ref, marq, mod, coule,ch);
        marque.setText("");
        modele.setText("");
        reference.setText("");
        couleur.setText("");
        nbplaces.setText("");
    }
