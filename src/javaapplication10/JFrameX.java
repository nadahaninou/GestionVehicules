    private void BttADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BttADDActionPerformed
       switch(typeVehicule.getSelectedItem().toString()){
           case "voiture":
           {
               try {
                   voiture();
               } catch (SQLException ex) {
                   Logger.getLogger(JFrameX.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
               break;

           case "camionete":
           {
               try {
                   camionete();
               } catch (SQLException ex) {
                   Logger.getLogger(JFrameX.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
               break;

           case "minibus":
           {
               try {
                   minibus();
               } catch (SQLException ex) {
                   Logger.getLogger(JFrameX.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
               break;

       }
    }//GEN-LAST:event_BttADDActionPerformed

    private void typeVehiculeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_typeVehiculeActionPerformed
            if(typeVehicule.getSelectedItem().toString().equals("voiture")){
                chargemax.setVisible(false);
                nbplaces.setVisible(false);
                ch.setVisible(false);
                nb.setVisible(false);
               
            }
            if(typeVehicule.getSelectedItem().toString().equals("camionete")){
                chargemax.setVisible(true);
                nbplaces.setVisible(false);
                nb.setVisible(false);
                ch.setVisible(true);
               
            }
            if(typeVehicule.getSelectedItem().toString().equals("minibus")){
                chargemax.setVisible(false);
                nbplaces.setVisible(true);
                nb.setVisible(true);
                ch.setVisible(false);
            }
            
            
            
    }//GEN-LAST:event_typeVehiculeActionPerformed

